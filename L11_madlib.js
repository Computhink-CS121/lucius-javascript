let button1;
let story;
function setup() {
createCanvas(600,600); // width, height
background("black");


button1 = createButton("Click on me!");
button1.position(50,50)
button1.size(100,80)
button1.mousePressed(changeText)
story = 0;
}
function changeText(){
    story++
}


function draw() {
   fill("hotpink");
   noStroke();
   rect(100,200,400,300,25)
   fill("white");
   //story = "HAPPY"
   textSize(48)
   textAlign(CENTER,CENTER);
   text(story, 300,320);
  
}



























































































































































































































































































































































































































































